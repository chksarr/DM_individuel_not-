package sodas;

/**
 * Énumération représentant différents goûts de soda.
 */
public enum Gout {
    /**
     * Goût de cerise.
     */
    CERISE,

    /**
     * Goût de citron.
     */
    CITRON,

    /**
     * Goût de cola.
     */
    COLA,

    /**
     * Goût de noix de coco.
     */
    COCO,

    /**
     * Goût de myrtille.
     */
    MYRTILLE;
}